//Numeros
// Declaración de constantes numéricas
const num1 = 100; // Declara y asigna 100 a num1
const num2 = 200; // Declara y asigna 200 a num2
const num3 = 8;   // Declara y asigna 8 a num3
const num4 = 5;   // Declara y asigna 5 a num4
const num5 = -5;  // Declara y asigna -5 a num5

// Realizando operaciones aritméticas y mostrando los resultados en consola

// Suma: Agrega num2 a num1
console.log(num1 + num2); // Muestra 300 en consola

// Resta: Resta num2 de num2
console.log(num2 - num2); // Muestra 0 en consola

// Multiplicación: Multiplica num3 por num2
console.log(num3 * num2); // Muestra 1600 en consola

// División: Divide num2 entre num2
console.log(num2 / num2); // Muestra 1 en consola

// Módulo: Calcula el residuo de dividir num5 entre num2
console.log(num5 % num2); // Muestra -5 en consola, ya que -5 dividido 200 da un residuo de -5
