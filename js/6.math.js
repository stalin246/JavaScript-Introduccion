// Objeto Math

let resultado;

// Math.PI devuelve el valor de PI.
resultado = Math.PI; // Aproximadamente 3.14159

// Math.round redondea al número entero más cercano.
resultado = Math.round(2.5); // Redondea a 3

// Math.ceil redondea un número hacia arriba al entero más próximo.
resultado = Math.ceil(2.1); // Redondea a 3

// Math.floor redondea un número hacia abajo al entero más próximo.
resultado = Math.floor(2.9); // Redondea a 2

// Math.sqrt calcula la raíz cuadrada de un número.
resultado = Math.sqrt(144); // Resultado es 12

// Math.abs devuelve el valor absoluto de un número.
resultado = Math.abs(-200); // Resultado es 200

// Math.min devuelve el mínimo de los argumentos proporcionados.
resultado = Math.min(1, 2, 3, 4, 5); // Resultado es 1

// Math.max devuelve el máximo de los argumentos proporcionados.
resultado = Math.max(1, 2, 3, 4, 5); // Resultado es 5

// Math.random devuelve un número aleatorio entre 0 (inclusive) y 1 (exclusivo).
resultado = Math.random(); // Devuelve un número aleatorio como 0.472931...

// Math.floor(Math.random() * 30) devuelve un número entero aleatorio entre 0 y 29.
resultado = Math.floor(Math.random() * 30); // Por ejemplo, podría ser 14

console.log(resultado); // Muestra el último resultado calculado, en este caso un número aleatorio entre 0 y 29
