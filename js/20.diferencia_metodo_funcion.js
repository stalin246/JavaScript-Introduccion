// Definición de variables
const num1 = 20;
const num2 = "20";

// Uso de la función 'parseInt'
console.log(parseInt(num2)); // Función parseInt: transforma una cadena de caracteres en un número. Aquí, "20" se convierte en 20.

// Uso del método 'toString'
console.log(num1.toString()); // Método toString: convertido en un método de un objeto o primitiva, aquí convierte el número 20 en la cadena de caracteres "20".
