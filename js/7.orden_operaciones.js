// Operaciones matemáticas

let resultado;

// Multiplica la suma de 20 y 30 por 2.
resultado = (20 + 30) * 2; // Primero suma 20 + 30 = 50, luego 50 * 2 = 100

// Multiplica la suma de 100, 200 y 300 por 0.2.
resultado = (100 + 200 + 300) * 0.2; // Primero suma 100 + 200 + 300 = 600, luego 600 * 0.2 = 120

console.log(resultado); // Imprime 120, el resultado de la última operación

// Incrementos y decrementos

let puntaje = 10; // Inicializa 'puntaje' con 10

// Operador de incremento postfijo: incrementa 'puntaje' en 1 después de que la expresión actual es evaluada.
puntaje++; // 'puntaje' ahora es 11

// Operador de incremento prefijo: incrementa 'puntaje' en 1 antes de que la expresión actual es evaluada.
++puntaje; // 'puntaje' ahora es 12

// Operador de decremento postfijo: decrementa 'puntaje' en 1 después de que la expresión actual es evaluada.
puntaje--; // 'puntaje' vuelve a ser 11

// Operador de decremento prefijo: decrementa 'puntaje' en 1 antes de que la expresión actual es evaluada.
--puntaje; // 'puntaje' ahora es 10 nuevamente

// Operador de asignación de suma: añade 10 a 'puntaje'.
puntaje += 10; // 'puntaje' ahora es 20

console.log(puntaje); // Imprime 20, el resultado final después de todos los incrementos y decrementos
